import { State, City, Ward, AQICategory, getAQICategory, PollutantData } from './pollutionData';

// Helper to generate random AQI within range
const randomAQI = (min: number, max: number) => Math.floor(Math.random() * (max - min + 1)) + min;

// Helper to generate pollutants based on AQI
const generatePollutants = (aqi: number): PollutantData => {
  const factor = aqi / 150;
  return {
    pm25: Math.round(aqi * 0.6 + Math.random() * 20),
    pm10: Math.round(aqi * 1.1 + Math.random() * 30),
    no2: Math.round(30 + aqi * 0.2 + Math.random() * 15),
    so2: Math.round(5 + aqi * 0.05 + Math.random() * 8),
    co: Math.round((0.8 + aqi * 0.008 + Math.random() * 0.5) * 10) / 10,
    o3: Math.round(40 + Math.random() * 25),
    nh3: Math.round(10 + Math.random() * 20),
  };
};

// Helper to generate wards for a city
const generateWards = (cityId: string, cityName: string, baseAqi: number, wardNames: string[], baseCoords: [number, number]): Ward[] => {
  const sources = ['Vehicular', 'Industrial', 'Construction', 'Waste Burning', 'Domestic', 'Stubble Burning'];
  return wardNames.map((name, i) => {
    const wardAqi = Math.max(20, Math.min(500, baseAqi + randomAQI(-50, 50)));
    return {
      id: `${cityId}-${name.toLowerCase().replace(/[\s.]/g, '-')}`,
      name,
      aqi: wardAqi,
      category: getAQICategory(wardAqi),
      pollutants: generatePollutants(wardAqi),
      population: randomAQI(50000, 500000),
      area: Math.round((5 + Math.random() * 25) * 10) / 10,
      dominantSource: sources[Math.floor(Math.random() * sources.length)],
      coordinates: [baseCoords[0] + (Math.random() - 0.5) * 0.1, baseCoords[1] + (Math.random() - 0.5) * 0.1] as [number, number],
    };
  });
};

