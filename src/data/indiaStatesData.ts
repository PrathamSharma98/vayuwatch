// Helper functions for the expanded dataset
export const getAllIndiaStates = (): State[] => allIndiaStates;

export const getAllIndiaCities = (): City[] => {
  return allIndiaStates.flatMap(state => state.cities);
};

export const getAllIndiaWards = (): { ward: Ward; city: City; state: State }[] => {
  const result: { ward: Ward; city: City; state: State }[] = [];
  allIndiaStates.forEach(state => {
    state.cities.forEach(city => {
      city.wards.forEach(ward => {
        result.push({ ward, city, state });
      });
    });
  });
  return result;
};

export const getIndiaStats = () => {
  const allCities = getAllIndiaCities();
  const totalAqi = allCities.reduce((sum, city) => sum + city.aqi, 0);
  const avgAqi = Math.round(totalAqi / allCities.length);
  
  const categoryCounts = allCities.reduce((acc, city) => {
    acc[city.category] = (acc[city.category] || 0) + 1;
    return acc;
  }, {} as Record<string, number>);

  const sortedCities = [...allCities].sort((a, b) => b.aqi - a.aqi);

  return {
    averageAqi: avgAqi,
    category: getAQICategory(avgAqi),
    totalCities: allCities.length,
    totalStates: allIndiaStates.length,
    totalWards: getAllIndiaWards().length,
    categoryCounts,
    worstCity: sortedCities[0],
    bestCity: sortedCities[sortedCities.length - 1],
    top5Polluted: sortedCities.slice(0, 5),
    top5Cleanest: sortedCities.slice(-5).reverse(),
  };
};
